package edu.wctc.dj.week4.model;

/**
 *
 * @author Tyler
 */
public class Product {
    
    private String id;
    private String name;
    private Double cost;

    public Product(String id, String name, Double cost) {
        this.name = name;
        this.cost = cost;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getCost() {
        return cost;
    }

    public void setCost(Double cost) {
        this.cost = cost;
    }
    
    
}
